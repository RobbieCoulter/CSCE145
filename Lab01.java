public class Lab01 {

	public static void main(String[] args) {
		System.out.println(" _  _");
		System.out.println("( `^`))");
		System.out.println("|    ||");
		System.out.println("|    ||");
		System.out.println("'----'`");
		systemPrintln(" _  _", "   _  _" );
		systemPrintln("( `^`))", "( `^`))");
		systemPrintln("|    ||", "|    ||");
		systemPrintln("|    ||", "|    ||");
		systemPrintln("'----'`", "'----'`");
	}

	private static void systemPrintln(String string1, String string2) {
		System.out.println(string1 + " " + string2);
		
	}
}
